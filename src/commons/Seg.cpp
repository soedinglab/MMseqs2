void Seg::getwin_init(void)
{
    char    *cp, *cp0;
    int             i;
    char    c;

    for (i = 0; i < sizeof(aaindex)/sizeof(aaindex[0]); ++i)
    {
        aaindex[i] = 20;
        aaflag[i] = TRUE;
    }

    for (cp = cp0 = "ACDEFGHIKLMNPQRSTVWY"; (c = *cp) != '\0'; ++cp)
    {
        i = cp - cp0;
        aaindex[c] = i;
        aaindex[tolower(c)] = i;
        aachar[i] = tolower(c);
        aaflag[c] = FALSE;
        aaflag[tolower(c)] = FALSE;
    }
    return;
}

