char* Seg::maskseq(const char *fastaseq)
{
    Segment *segs;
    Sequen *seq = openseq(fastaseq);

    segs = (Segment *) NULL;
    segseq(seq, &segs, 0);
    mergesegs(seq, segs);
    char *masked = singreport(seq, segs);

    freesegs(segs);
    closeseq(seq);

    return masked;
}

